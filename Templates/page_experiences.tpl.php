        <h2 class="one" >Réception pour l'anniversaire de l'empereur <time datetime="2023-03-18">le 18 mars 2023</time> </h2>
	    <p class="two" > Jusqu'à récemment mes activités avec l'ambassade du Japon me permettaient d'être invité à la réception annuelle à l'occasion de l'anniversaire de l'empereur.<br>
            Point notable supplémentaire, notre anniversaire tombait les même jour et mois, le 23 décembre.
            Changement avec le nouvel empereur, ce n'est plus le jour anniversaire que nous avons en commun mais le jour et l'année de naissance.
            Ce dernier étant né en début d'année, celà permet de faire la réception par un temps plus clément et ainsi de profiter du jardin.
            Très belle réception encore cette année, Pierre Hermé était invité et à été décoré par M.l'Ambassadeur.
            Plein de bonnes choses et, bien sûr, quelques sake a déguster.
            Je vous reparlerais du Mizubasho, dont j'ai un peu abusé, du Asamurasaki, sans odeur mais d'une belle couleur orange/rosé, et du Kagatori.<br>
            Mais le sujet du jour c'est bien le Shichikanba 七冠馬.<br>
            La chose intéressante est que le logo de cette marque ressemble à un fer à cheval (teitetsu 蹄鉄) car cette marque est liée à une course de chevaux (kyousouba 競走馬).
            Ce que j'ai appris le lendemain en me rendant sur le site.
            Annecdote, comme la réception se terminait et que je discutais encore avec la dame qui présentait ce sake elle m'a donné la bouteille entammée.
        </p>

