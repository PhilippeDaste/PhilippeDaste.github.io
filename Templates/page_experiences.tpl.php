        <h2 class="one">Je bois à une belle journée : shirakabekura 白壁蔵 </h2>

        <p class="two">Le nom ne m'est pas in connu mais je n'ai pu visiter cette maison lors de mon passage à Kobe : trop de pluie ce jour là.
            L'entreprise est intéressante, sur son site on trouve clairement affiché une vocation écologiste (kankyoukatsudou 環境活動).
            Certes, c'est le cas de nombreuses marques de sake qui insistent souvent sur la pureté des ingrédients : riz, eau, levure.
            Mais parmis les grands producteurs c'est encore une rareté.
            Deux marques dominent : Mio et Shirakabekura.<br>
            <a href="http://shirakabegura-mio.jp/" target="_blank"> Mio</a> 澪 (remou, sillage) est une marque de sake pétillant lancée en 2011.
            On la trouve assez communément en France.
            Elle titre 5%, ce qui en fait une intéressante boissons d'apéritif.<br>
           Shirakabekura est certainnement moins connu et ma surprise a été grande de'en trouver dans une boutique du 13 ème arrondissement.
           D'autant plus intéressante a été cette trouvaille  que c'est le Kimoto de la maison qui était en vente : l'occasion de faire coup double.
           Bien entendu, le prix aussi était doublé... 21,80€ au lieu de 1200 Yens (10€ environ).
        </p>
        
