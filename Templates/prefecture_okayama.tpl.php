    <p>
        En premier lieu le célèbre pont se Seto (瀬戸大橋) qui relie Honshu à Shikoku<br>
        Avec Mari nous y sommes passés le lendemain de l'inauguration (10 avril 1988) sans accueil ni flonflon d'aucune sorte.
        Venant de Okayama ou nous avions visité le célèbre jardin Korakuen (後楽園), nous allions à Takamatsu visiter le tout aussi célèbre jardin Ritsurin (栗林公園).
        Le Korakuen, réputé comme un des 3 plus beaux du Japon est certes étendu et offre une variété de paysages "traditionnels" mais de ce fait manque un peu d'intimité.
        La présence du chateau, très joli vu de loin, mais rasé en 45 et refait en béton, me fait trop penser à Disneyland.
        Je le classe loin derrière le Ritsurin.
    </p>

    <p>
        Revenons au sujet.<br>
        Relativement peu de brasseries dans la préfecture : 40 selon l'association des brasseurs (酒造組合) - sur son site non sécurisé...:<br>  
        <a href="http://www.okasake.com/" target="_blank">Association sake Okayama</a><br>
        Ceci pourrait sembler étonnant dans la mesure ou ce département est le premier producteur de la variété de riz Omachi (雄町) avec 95% de la production.
        Un type de riz ayant ses "afficionados".
        Avec 160 000 tonnes de production de riz, ce département est loin du top 10.
        Mais, information non confirmée (par le site japancrops) il est noté comme le troisième département de l'archipel en terme de production de riz à sake.
    </p>

    <p>
        La préfecture est née, lors de la restauration Meiji de la réunion des trois provinces de Bizen, Bitchuu et Mimasaka.
        Une distinction que conserve l'association des producteurs de sake.
        Bizen (備前), dont le centre est à Okayama m'est connu, de nom, depuis longtemps puisque c'est certainement la province la plus célèbre du Japon pour sa production de sabre.
        Inévitablement on y trouve le plus grand nombre de brasseries.
        Je n'ai pu hélas que tester (180ml) une édition limitée (限定) de la brasserie Toshimori (利守) dans une boutique de la gare de Okayama.
        En dehors du fait qu'il s'agissait d'un junmai avec un taux de polissage de 60%, rien de particulier.
        Ce qui me permet ici de faire la remarque du manque de précision dans l'étiquetage du sake. Le riz était japonais mais sans sa variété.
        Même chose pour le koji.
        C'est bien trop insuffisant pour marquer une édition limitée.
        Mais ce problème est très général à l'étiquetage du sake.<br> 
        Bichu (備中), de son côté, a pour point d'orgue la ville (le port) de Kurashiki. Il se visite un spot, considérablement aménagé, avec d'anciens canaux et entrepôts de marchands.
        Le Fort joli, nous avons constaté qu'il a encore été rénové par rapport à notre première venue et séjour en 88. Probablement un peu trop.<br>
        En matière de sake, essayé un junmai amakuchi (辛口), local (倉敷の地酒), de la marque Sanzen (燦然) issu directement de la brasserie locale de Kikuchi (菊池).
        Moins connu et bien moins accessible, le chateau de Takahashi (高梁). Apellation souvent interchangée avec celle de Bitchu Matsuyama (備中松山).
        Il se mérite, après un trajet en train longeant la rivière Takahashi et une belle escalade.
        Un authentique chateau de montagne, sauvé par son éloignement des grandes villes et les efforts de sa population dont les enfants ont monté une à une les tuiles nécessaires à sa première restauration en 1940.<br> 
        La troisième province, Mimasaka (美作) est essentiellement connue pour être le lieu de naissance de Miyamoto Musashi (宮本 武蔵). 
    </p>

