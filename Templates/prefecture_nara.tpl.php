    <p>
        Nous sommes ici au coeur du Japon ancien.Un des départements les plus riches du Japon en terme de trésors culturels et naturels.
        La préfecture de Nara à la particularité d'avoir trois sites classés au Patrimoine mondial de l'UNESCO.
        En premier lieu la ville de Nara et ses trésors, l'ensemble boudhiste du Horyu-ji et la province de Kii avec le mont Yoshino.<br>
        Mais les trésors ne s'arrêtent pas là puisque c'est Miwa (三輪) et le temple Omiwa (大神神社) qui attirent les producteurs de sake de tout le Japon en novembre.
        Le mont Miwa (三輪山) est une colline proche de la ville de Sakurai.
        C'est une montagne historiquement et religieusement importante au Japon, en particulier au cours de son histoire ancienne.
        Il constitue un lieu saint du shintoïsme. Et du sake...
    </p>

