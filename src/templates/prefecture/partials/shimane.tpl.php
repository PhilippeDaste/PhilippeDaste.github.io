    <p>J' y suis passé en 1987 et n'avais à ce momment là aucun intérêt pour le sake.
        Ce ne sont pourtant pas les brasseries qui manquent comme le montre le site de l'Associationdes fabricants de sake :
        <a href="//shimane-sake.or.jp/en/sakebreweries/" target="_blank">Association sake Shimane</a>
        Shimane est d'ailleurs considéré par certains comme le lieu d'origine du sake car dans le Kojiki (古事記) on trouve de nombreuses scènes relatives à la fabrication du sake dans cette province.
        Nous sommes à proximité de Izumo.<br>
        Je me garderais d'entrer dans une discussion d'experts entre Shimane et Nara.<br>
        Sur l'agriculture dans la préfecture se rendre sur l'excellent site Japan CROPs :  <a href="https://japancrops.com/en/prefectures/shimane/" target="_blank">Japan CROPs</a>. 
        Site que l'on peut d'ailleurs solliciter pour toutes les préfectures du Japon. Il a pour vocation la promotion des produits agricoles du Japon et donne de nombreuses statistiques.
        Avec 80 000 tonnes de production sur 17 000 ha Shimane est très loin du top 10 des préfectures cultivant du riz.
        La production du pays se situe à 7,5 millions de tonnes.<br>
        Aucune information sur les types produits mais il semble que le Omachi (雄町) soit assez répandu et utilisé par les producteurs de sake.
        L'essentiel de la production de riz allant bien entendu vers l'alimentation.

