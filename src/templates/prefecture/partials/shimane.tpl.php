            Comme pour beaucoup de marques de sake, la recherche d'un nom original, poétique ou autre conduit à des découvertes intéressantes.
            ふそう (扶桑) se réfère soit à un arbre divin, la présence du murier en atteste (くわ 桑), soit au nom que les chinois donnaient à une île mystérieuse à l'est de la Chine.
            Certains y voient les côtes de l'amérique, d'autres, plus simplement le Japon.
            Dans plusieurs chroniques très anciennes, le Japon est donc une des interprétations donnes à Fusang (扶桑) mais ceci est plus mythique que réel.
            Dès le début du premier millénaire, avec l'arrivée en Chine des premières ambassades c'est le terme 倭 (wa/wo/wai) qui est utilisé.
            Considéré péjoratif (soumis, nain) ce caractère est remplacé par les japonais, aux alentour des années 600~650 par le terme  Wa 和 (harmonie).
            Evidemment considéré comme plus flatteur que l'inélégant (不雅) caractère 倭.
