<p>Je n'ai jamais mis le pied dans ce département. Il est vrai qu'en matière de "biens culturel" il a peu à proposer.</p>
<p>Par contre, il est connu comme le pays des neiges. Cette culture est entretenue depuis les temps anciens et est toujours vivante.
Dans la préfecture de Niigata, de nombreuses rivières, dont les rivières Shinano et Agano, traversent les vastes plaines fertiles et se jettent dans la mer du Japon.
Le fleuve Shinano (信濃川) est d'ailleurs le plus long cours d'eau japonais avec ses 367 kilomètres de long.<br>
Sur l'agriculture, comme pour toutes les préfectures se rendre sur l'excellent site Japan CROPs :  <a href="https://japancrops.com/en/prefectures/niigata/" target="_blank">Japan CROPs</a>. 
Le département de Niigata est le premier producteur de riz du Japon, ayant dépassé Hokkaiko. Avec 620 000 tonnes, il représentait en 2021 environ 8.2% de la production nationale (7.5 M tonnes).
Pour l'alimentation, le Koshihikari (越光 ) est considéré comme la variété n°1.<br>
Pour ce qui est du sake, le département est classé n°3, derrière Gunma et Kyoto en terme de production. C'est ici le Gohyakumangoku (五百万石) qui est produit et utilisé de préférence.
On trouve aussi le koshitanrei (越淡麗), un croisement entre le gohyakumangoku et le Yamadanishiki (山田錦).</p>
<p>Neige et froid ont deux conséquences essentielles sur la production de sake : eau très pure et brassage à basse température.
Une eau très pure est définie comme ayant moins de 5mg/l d'éléments minéraux, comme le calcium, le magnésium ou le fer.
C'est le cas pour les sake de Niigata, en opposition aux sake de Nada, connus pour leur eau plus riche en minéraux. (on pourrait dire Volvic vs Contrexeville)
C'est certainement vrai pour nombre de sake du département. J'ai pourtant une interrogation pour les brasseries plus en bord de mer.
Le terme (潟: kata: lagune) est présent dans l'étymologie et se constate sur toutes les cartes.
Or la lagune peu être en échange plus ou moins permanent et visible avec la mer ; influençant éventuellement la composition de l'eau ?</p>
<p>L'autre élément, la basse température de brassage, est bien plus contrôlable de nos jours.</p>
<p>
L'association des brasseurs de sake du département <a href="https://www.niigata-sake.or.jp/" target="_blank">association brasseurs niigata</a>  va un peu vite à mon avis en généralisant la notion de Tanrei (淡麗) : doux et rafraichissant.
C'est certainement ce qui a fait la célébrité de la marque Kubota. Mais l'association recense 90 brasseries, peu probable qu'elles soient toutes sur cette même ligne. <br>
Pour mieux distinguer le sake de Niigata, en février 2022 a été mis en place un label d'indication géographique (GI) propre au département.</p>

