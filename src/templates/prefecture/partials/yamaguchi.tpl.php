    <p>
        Préfecture la plus à l'ouest de l'île de Honshu, elle est connue comme le domaine de Chōshū (長州藩, Chōshū-han).
        En effet, celui-ci occupait la totalité de l'actuelle préfecture et a été le principal artisant de la chute du shogunat Tokugawa.<br>
        Pour l'histoire, aussi, la première messe de Noël fut célébrée à Yamaguchi en 1552.
        Comment Saint François Xavier (1506-1552), missionnaire catholique et prêtre jésuite s'est-il retrouvé à Yamaguchi pour y précher ?
        C'est dans la ville même de Yamaguchi que l'on trouve l'essentiel des attractions de la préfecture :
        le mémorial Saint François Xavier, le Joei ji, le parc Kozan (香山) et sa pagode à 5 étages (rurikoji).
        Pourtant, pour l'anecdote, la seule chose dont je me souvienne c'est qu'avec Mari nous avons visité les cavernes et le plateau calcaire de Akiyoshi.
        A à quelques km de Yamaguchi, ce site est assez peu connu des japonais comme des étrangers.
        Je me souviens pourtant encore de la transition entre la fraicheur de la caverne (17°C) et la chaleur qui régnait sur le plateau.<br>
        Pour le reste, il faut se rendre à Hagi.
        Ou à Iwakuni, pour y admirer son célèbre pont : le Kintai kyo (錦帯橋). A 3/4h de la ville, en train local et taxi, on peut se perdre dans la campagne afin de visiter la brasserie Asahi (旭).<br>
        A lire l'ouvrage "Choshu in the Meiji restauration" de Albert M.Craig (Lexigton Books 1992) on comprend comment le clan s'est trouvé au coeur des changements.
        Ce qui interroge alors c'est sa discrétion, avant, et son effacement, après, pour rester un département dont le rayonnement culturel et économique est très minime. 
    </p>

