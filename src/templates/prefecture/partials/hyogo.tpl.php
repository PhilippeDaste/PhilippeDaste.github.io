    <p>
        Sur l'agriculture dans la préfecture se rendre encore sur le site Japan CROPs : <a href="https://japancrops.com/en/prefectures/hyogo/" target="_blank">Japan CROPs</a>.
        Avec 175 000 tonnes de riz produites, le département se maintient au 15 ème rang ces 10 dernières années.
        Il est pourtant le premier producteur de riz à sake, avec 28% de la production japonaise.
        Notamment pour ce qui est du riz Yamada nishiki.
        Une étude du MAFF (Ministry of Agriculture, Forestry and Fisheries) indique qu'en 2023, sur les 28 000 tonnes produites au Japon, 15 800 proviennent de Hyogo, quasiment 60 % de la production totale du pays.
        <a href="https://www.maff.go.jp/j/seisaku_tokatu/kikaku/attach/pdf/sake-7.pdf/" target="_blank"></a>
        Ce n'est pas une surprise.
        C'est à partir de 1923, dans ce qui est aujourd'hui la ville de Kato (加東), sous la direction de Teiji Fujikawa (禎次 藤川), qu'ont débuté des essais par croisement de variétés.
        Ce sont les Yamadaho (山田穂) et Tankan Watari-bune (短稈渡舟) qui ont été utilisés, avec le Watari-bune, à tige courte, comme père.
        C'est la souche Yamadawari 50-7 qui s'est avérée être la variété la plus performante, donnant naissance au Yamada nishiki en 1931.
        A l'intérieur du conté de Kato, Akitsu (秋津) est considéré comme le summum des rizières produisant le Yamada nishiki.
        
    </p>

