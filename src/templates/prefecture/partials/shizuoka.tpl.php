    <p>
        Avec un peu moins de 80 000 tonnes, la production de riz dans la préfecture de Shizuoka semble minuscule.
        Ce qui n'empêche pas l'innovation ! A cet egard, la naissance du Homare fuji (誉富士) est très intéressante.
        <a href="https://homarefuji.com/" target="_blank">Homare Fuji</a>.
        En en 1998, les chercheurs ont irradié le riz de semence de Yamada Nishiki avec des rayons gamma et cultivé la première génération de plants.
        Le nom a été choisi par le gouverneur de la préfecture d'Ishikawa à l'issue d'un concours public.
        Il a été accepté en 2006 et les droits de la variété mère sont nés.
        En 2007, la zone d'essai a été étendue aux villes d'Izunokuni et de Numazu, avec un total de 15,7 ha (30 agriculteurs) cultivés.
        J'ai hate de goûter un sake qui emploie ce riz, le site les recense.<br>
        Les brasseries de sake de la préfecture sont données par l'association : <a href="https://japansake.or.jp/sakagura/en/shizuoka/" target="_blank">association brasseries sake Shizuoka</a>. 
        27 brasseries, c'est relativement peu. Lieu de passage obligé au pied du mont Fuji, faire un arrêt pour contempler la montage en sirotant un bon sake faisait partie des agréments du voyage de et vers la capitale le long du Tokaido.
        C'est certainement encore vrai de nos jours<br>
        Pour ceux qui n'aimeraient pas les boissons alcolisées, la pause devant le Fuji peut se faire avec un très bon thé.
        Shizuoka est, avec 14 700 ha le plus important producteur de thé du Japon. Loin devant Kagoshima (8 500 ha).
