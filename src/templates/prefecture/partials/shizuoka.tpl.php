    <p>
        L'eau pure est indispensable à la production du sake, elle est aussi une condition indispensable à la culture du Wasabi.<br>
        Shizuoka est également réputé pour sa production de Wasabi (山葵), son autre trésor vert.
        Curieusement, ni le thé, ni le wasabi ne se retrouvent sur le site Japan CROPS.
        La préfecture produit environ 230 tonnes de wasabi et représente 40% de la production nationale.
        La culture, originaire du district de Aoi, s'est déportée au fil des ans de l'autre côté de la baie de Suruga, vers la péninsule d'Izu, hors de l'empreinte montagneuse du Fuji.
        La “culture traditionnelle du wasabi à Shizuoka”, par la méthode Tatamiishi (畳石式), a d'ailleurs été reconnue en tant que Système ingénieux du Patrimoine Agricole Mondial (SIPAM) en 2018.
        <a href="https://www.shizuoka-wasabi.jp/" target="_blank">Wasabi à Shizuoka</a>
        Près des champs de wasabi de Ikadaba (筏場筏場) se trouve, au centre de Izu, la ville de Shuzenji et le temple du même nom (修禅寺).
        Ce dernier est un des temples fondés par Kukai (空海), père du mouvement Shingon et un des plus grands moines bouddhistes que le Japon ait connu.
        On pourra aussi à Shuzenji se procurer les produits de la brasserie Bandai :<a href="https://izu-bandai.com/index.html" target="_blank">brasserie Bandai</a>.
