    <p>
        Relativement peu connue et fréquentée des étrangers, la province de Yoshino est la plus grande et la plus au sud de la préfecture.
        Le long de la rivière Yoshino qui, orientée est/ouest, va se jeter dans la mer à Wakayama on y trouve la brasserie Miyoshino (美吉野醸造), et sa marque bien connue : Hanatomoe (花巴).
        <a href="http://www.hanatomoe.com/index.html" target="_blank">Brasserie Miyoshino</a><br>
        A Yoshino, se trouve le temple de Shoryaku-ji (正暦寺), un lieu où pendant la période Muromachi (de 1336 à 1573) un groupe de moines produisaient leur propre saké.
        Pour éviter que le moromi (le riz en fermentation) ne se gâte, les moines avaient trouvé l’astuce de démarrer la fermentation avec de l’eau de source dans laquelle avait trempé une certaine quantité de riz.
        Il a été démontré plus tard que ce trempage permettait d’augmenter de façon significative la concentration en acide lactique.
        Or ce composé est indispensable pour prévenir la pousse bactérienne au début de la fermentation.
        Ainsi, par cette méthode, ils assuraient la bonne santé de leur production. Cette technique bien particulière est appelée « Bodaïmoto ».
        Des écrits anciens rapportant les détails de cette méthode ont été découverts par hasard et ont fait l’objet d’études et de mises en pratique.
        La brasserie de Miyoshino Jozo est l’une des rares au Japon à l’employer pour l’élaboration de ses sakés.
        Voici un site intéressant sur le sake dans la province de Nara : <a href="https://www.pref.nara.jp/miryoku/ikasu-nara/en/seishu/shoryakuji/" target="_blank">Article prefecture Nara</a><br>
