    <p>Certainement pas la préfecture la plus connue du Japon, mais tout aussi certainement celle ou j'ai été le plus souvent du fait de la présence des parents de ma femme.</p>
    <p>Etablies peu après l'abolition des clans en 1871, les préfectures de Tochigi et Utsunomiya ont remplacé la province de Shimotsuke.
    La préfecture a pris le nom de Tochigi et ses limites actuelles en 1876. La capitale s'est établie à Utsunomiya en 1884. Tout le monde est satisfait...
    Petit clin d'oeil à la France, le nom, et le symbole de de département font appel au maronnier [du Japon] : 栃.
    Je ne peux toutefois dire en avoir vu beaucoup lors de mes promenades à vélo, en plaine comme en montagne. Mais la forêt représente 55% du département.
    Par contre, les azalées, fleur symbole du département, sont bien présentes et fleurissent joliement au début du printemps.
    </p>
    <p>C'est la forêt, ici de cyprès, qui abrite les sanctuaires de Nikko, bien connus des touristes.
    Il faut ici remercier Tokugawa Ieyasu pour s'être fait enterrer dans le sanctuaire de Nikko Toshogu (日光東照宮), on aurait peine à trouver un autre trésor culturel dans cette province.<br>
    Autre petite célébrité du département, le parc floral de Ashikaga. A visiter de préférence entre mi avril et mi mai pour sa fabuleuse glycine agée de 150 ans.
    </p>

    <p>Avec 300 000 tonnes de riz produits, Tochigi se place en huitième place parmis les départements.
        L'association des brasseurs de sake de Tochigi <a href="https://japansake.or.jp/sakagura/en/tochigi/" target="_blank">association brasseurs Tochigi</a> recense 30 brasseries.
        Ce n'est pas négligeable mais ne fait pas de Tochigi une région remarquable en la matière.</p>

    <p>
        Du fait de très nombreuses visites dans cette préfecture j'ai eu l'occasion que de tester plusieurs marques de sake :<br>
        <ul>
            <ui>Brasserie  <a href="https://www.tentaka.co.jp/index2.php/" target="_blank">Tentaka (天鷹酒造)</a> 〒324-0411 栃木県大田原市蛭畑2166.<br>
                Ces dernières années, la brasserie Tentaka s'est tournée vers l'organique (有機), c'est à dire du riz cultivé sans pesticides.
                Certainement bon pour l'environnement et peut-être la santé des amateurs, je n'ai pas constaté le moindre impact sur le goût de leur sake.
                Curieusement, sans se distinguer de tous les autres brasseurs, ils n'en profitent pas pour mettre explicitement en avant les variétés de riz qu'ils utilisent. 
                Par exemple, pour leur sake Junmai daiginjo (純米吟醸原酒) utilisant la variété de riz Yume sasara (夢ささら) c'est le procédé global hiyaoroshi (ひやおろし) qui est mis en avant.</ui><br>
                Le Yumesasara est pourtant une variété de riz adaptée à la fabrication du saké, développée par la préfecture de Tochigi sur une période de plus de dix ans.
                Pour l'anecdote, et sans qu'il y ait de rapport le prénom japonais de mon fils est Takahiko, Taka (鷹) signifiant le faucon. <br>

            <ui>Brasserie <a href="http://senkin.co.jp/" target="_blank">Senkin (仙禽)</a> 〒329-1321 栃木県さくら市馬場106.<br>
                Facilement reconnaissable à son logo, la brasserie met en avant, comme le vin, la notion de domaine (領域).
                Plusieurs variétés poussent sur le domaine : Kame no oo (亀ノ尾), Yamada nishiki (山田錦), omacchi (雄町). 
                La brasserie s'attache à la cohérence entre l'eau utilisée lors de la pousse et lors du brassage du riz.
                Ces dernières années elle s'est particulièrement attachée au Kame no oo.
                Tous leurs sakés sont des kimotos (sans ajout d'acide lactide), genshu (sans dilution), muroka (sans collage).
                Personnellement j'apprécie leur junmai daiginjo muroka (無濾過) kirifuri (霧降) pour son discret pétillant.
                Il est fait avec du Yamada nishiki poli à 50%.
                Une curiosité que je n'ai encore pas vue : leur Dolce Bouquet, un sake élaboré avec de la levure de vin.<br>
            
            <ui>Brasserie <a href="https://www.kyokuko.net/" target="_blank">Watanabe (渡邉)</a> 〒324-0212 栃木県大田原市須佐木797-1. Marque Kyokuko (旭興).<br>
                Implantée à côté de Otawara, j'avoue avoir essayé plusieurs fois leur sake ; sans arriver à en trouver une particularité marquante.<br>

            <ui>Aussi implantée à Otawara, la brasserie Shiraso.
                <a href="https://www.shiraso.com/" target="_blank">Shiraso 白相</a> 〒324-0501 栃木県那須郡那珂川町小川715-2. Marque Tochiakane とちあかね (栃茜).<br>
                Je n'ai pu goûter qu'un petit 300 ml de 特性本醸造, non répertorié sur leur site, qui utilise, si on se fie à l'image sur le bouchon, une levure de nadeshiko (撫子酵母) 
