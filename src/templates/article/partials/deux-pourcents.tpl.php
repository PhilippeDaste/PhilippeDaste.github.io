<p class="two">Je n'ai jamais encore mis les pieds à Niigata. <br>
    Il y a 30 ans j'ai discuté de Niigata avec un client originaire de la ville.
    Il m'avait expliqué qu'il faisait de la pêche sur glace et que de temps en temps quelqu'un disparaissait tout à coup mais que personne ne s'inquiétait car la lagune (?) était peu profonde et que le risque de noyade était nul.<br>
    Je n'en connais pas plus.
    C'est donc à organiser début 2024, lorsque je serais en retraite et au Japon.
    Il va nous falloir aller au festival de la neige de Tokamachi, mi-février.<br>

    Une marque domine : Kubota (久保田).<br>
    <a href="https://www.asahi-shuzo.co.jp//" target="_blank"> Kubota</a>
    Avec Dassai, c'est ma marque préférée. Et de façon curieuse, les deux brasseries ont le même nom (en français uniquement : Asahi)<br>
    Les marques Senjyu, un ginjo et  Hyakujyude, un tokubetsu honjozo, de la brasserie Asahi (朝日酒造) sont nées en 1985.
    Le junmai daiginjo, Manjyu, mon préféré, est né en 1986, l'année de mon arrivée au Japon.
    Aucun rapport puisque j'avais à l'époque à peine entendu parler du sake et certainement jamais goûté ! 
    Sans aucune intention au départ, la popularité de cette marque a rendu célèbre le concept de tanrei karakuchi (淡麗辛口).<br>

   Autre marque bien connue de la préfecture : Hakkaisan (八海山).<br>
   <a href="https://www.hakkaisan.com/" target="_blank">Hakkaisan</a>
   Je l'ai essayée mais n'ai pas apprécié le junmai daiginjo acheté à l'aéroport.
   Le mélange de riz (Yamadanishiki, Miyamanishiki, Gohyakumangoku, autre) n'était pas pertinent ? 
   C'est l'ensemencement avec la levure Akitakonno No. 2 qui ne l'était pas ??
   Essai à renouveler, éventuellement avec leur tokubetsu junmai, basé exclusivement sur le gohyakumangoku.
   Une chose est sûre, ils sont globalement plus précis sur leurs ingrédients que la plupart des brasseries.<br>
   Deux brasseries sur la centaine répertoriée dans le département : peut et doit mieux faire !
</p>

