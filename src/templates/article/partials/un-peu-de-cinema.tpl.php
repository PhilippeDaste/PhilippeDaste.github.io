<p class="two">Shizuoka c'est d'abord un souvenir de promenade dans le champ de thé de la famille Fukuzawa avec vue sur le mont Fuji.
    C'était à l'occasion du nouvel an 87/88 et je n'ai pas oublié les daruma mis au feu et le Zōni (雑煮 ou ぞうに) du nouvel an.
    C'était à Shimizu, ville absorbée par Shizuoka en 2003.<br>


    La préfecture est connue pour ses sake et je ne pourrais jamais tout essayer. La brasserie Sanwa (三和), par son site web, permet de commander en ligne.
    <a href="http://garyubai.com/" target="_blank"> Sanwa</a>
    C'est le nom de la marque principale de cette brasserie, Garyubai, qui m'a fait penser au cinéma.
    Et pour être précis aux sept samourai (七人の侍) de Kurosawa. L'adjoint du chef des samourai se nomme Gorobei (五郎兵衛).
    Impossible de mélanger pour un japonais ; mais pour moi, l'association, pour ne pas dire la confusion a été immédiate.
    Gorobei, de son vrai nom Yoshio Inaba, est un acteur de second plan mais dont la carrière a cependant été assez fournie.
    Dans le film il meurt soudainement d'un coup de fusil, sans avoir pu montrer ses talents.
    Restant le dragon qui n'a pas réussi à déployer ses ailes.<br>
    Et c'est là que l'on peut se reconnecter au sake.<br>
    Le terme Garyu (臥龍) est ancien et trouve ses origines dans le célèbre roman chinois des trois royaumes [dont on ne trouve hélas aucune traduction française correcte]. 
    Dans le roman, le dragon couché (臥龍) est une métaphore pour Zhuge Liang, un héros qui n'a pas encore eu l'occasion de développer ses ambitions et qui est caché dans le peuple.<br>
    Tout comme le pauvre Gorobei ?

