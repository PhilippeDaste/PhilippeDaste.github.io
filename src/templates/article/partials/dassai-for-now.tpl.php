
<p>
    C'est le Dassai qui a provoqué mon intérêt pour le sake.<br>
    Facile de s'en souvenir, j'étais invité avec Mari par un collègue japonais et nous avions regardé ensemble la finale du 100m des jeux olympiques de 2008, première victoire de Usain Bolt.
    Mauvaise photo et bouteille peu visible. J'avoue être moins doué que Ozu dont les bouteilles de bière Sapporo sont omniprésentes tout au long de son film le goût du sake.
    Référence pertinente sur ce site le titre du film, en japonais : "sanma no aji" (秋刀魚の味) n'a rien à voir.<br>
    Le nom de ce sake m'a interpellé : 獺祭. Le "festival de la loutre"
    Référence sympathique, la loutre japonaise a hélas officiellement été déclarée éteinte en 2012.
    Il en restait encore quelques spécimens en 1990 lorsque la marque Dassai a été lancée.
    Autre explication avancée sur le site : une référence au poéte Masaoka Shiki.
    Il était réputé pour éparpiller les haiku dans sa chambre, un peu comme la loutre le fait pour les poissons.
    Mon dictionnaire de kanji indique aussi une composition littéraire truffée de maximes, légendes ou références à d'anciens épisodes.<br>
    Quoi qu'il en soit, de très jolis kanji.
</p>

<p>
    La brasserie insiste sur le travail et le savoir faire de ses employés, notamment pour la fabrication du koji. C'est certainement vrai.
    Pour ma part, esprit peut-être trop scientifique, c'est l'utilisation de centrifugeuses (遠心分離器) pour le filtrage qui me semble faire la différence avec toutes les autres brasseries.
    Plutôt que le pressage ou l'égouttage, cette technique, chère et difficile, me semble idéale pour éviter une pression dommageable au grain et assurer une plus grande uniformité.<br>
    L'histoire du Dassai 23, racontée sur le site est intéressante. Ce produit né au 1er janvier 2000, sans bug, est effectivement d'une grande qualité.
    Je n'en ai bu qu'une bouteille, le Dassai 39 (39, san kyu, thank you) fait évidemment beaucoup plus partie de mon ordinaire.
</p>

